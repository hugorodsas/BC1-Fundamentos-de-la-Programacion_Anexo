public class Matriz3x3 {
    public static void main(String[] args) {
    int[][] numeros={{2,4,6},{3,6,9},{5,10,15}};
     for(int i=0;i<numeros.length;i++){
        for(int j=0;j<numeros[i].length;j++){
            System.out.print(numeros[i][j] + " ");
        }
    System.out.println("");
     }
    }
}
